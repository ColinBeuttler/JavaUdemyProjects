public class BooleanComparisons {
    public static void main(String[] args) {
        int chemistry = 95;
        int biology = 75;
        int english = 75;

        System.out.println(biology < chemistry);
        System.out.println(biology >= english);

        double sales = 37.55;
        double costs = 5.55;

        System.out.println(sales < costs);

        String sentence = "eggs";
        String sentence2 = "eggs";

        System.out.println(sentence2.equals(sentence));
    }
}
